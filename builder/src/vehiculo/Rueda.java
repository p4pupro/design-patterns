package vehiculo;

public class Rueda {

	
	
	
	
	public int diametro;
	public String llanta;
	public String neomatico;
	public int getDiametro() {
		return diametro;
	}
	public void setDiametro(int diametro) {
		this.diametro = diametro;
	}
	public String getLlanta() {
		return llanta;
	}
	public void setLlanta(String llanta) {
		this.llanta = llanta;
	}
	public String getNeomatico() {
		return neomatico;
	}
	public void setNeomatico(String neomatico) {
		this.neomatico = neomatico;
	}
	
	
}
