package vehiculo;

public class Carroceria {

	

	
	public boolean habitaculoReforazado=false;
	public String material;
	public String tipoCarroceria;
	public boolean isHabitaculoReforazado() {
		return habitaculoReforazado;
	}
	public void setHabitaculoReforazado(boolean habitaculoReforazado) {
		this.habitaculoReforazado = habitaculoReforazado;
	}
	public String getMaterial() {
		return material;
	}
	public void setMaterial(String material) {
		this.material = material;
	}
	public String getTipoCarroceria() {
		return tipoCarroceria;
	}
	public void setTipoCarroceria(String tipoCarroceria) {
		this.tipoCarroceria = tipoCarroceria;
	}
	
}
